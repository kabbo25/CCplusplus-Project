const int N = 10002;
const int M = 300;
const long long MOD = 495;
const double PI = acos(-1.0);
const double eps = 1e-10;
const int MAX_VAL = 800*1000;
char t[N];
string s;

int main(){
    getline(cin, s);
    int len_t = 0;
    memset(t, 0, sizeof(t));
    for (int i = 0; i < s.length(); ++ i) {
        if (s[i] == 'a' || s[i] == 'e' || s[i] == 'i' || s[i] == 'o' || s[i] == 'u') {
            t[len_t ++] = (s[i] - 32);
        } else if (s[i] == 'A' || s[i] == 'E' || s[i] == 'I' || s[i] == 'O' || s[i] == 'U') {
            t[len_t ++] = s[i];
        }
    }
    puts(t);
    return 0;
}
